function getSlices(data) {
    const dataSlices = [];
    let currentSlice = [];
    data.forEach(dataItem => {
        currentSlice.push(dataItem);
        if(currentSlice.length >= SAMPLE_SIZE) {
            dataSlices.push(currentSlice);
            currentSlice = [];
        }
    });
    return dataSlices;
}

